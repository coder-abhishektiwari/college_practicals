# Performing different arithmetic operations
print()
a = 15
b = 4

# Addition
add = a + b
print("Addition of", a, "and", b, "is:", add)  # Output: Addition of 15 and 4 is: 19

# Subtraction
subtract = a - b
print("Subtraction of", b, "from", a, "is:", subtract)  # Output: Subtraction of 4 from 15 is: 11

# Multiplication
multiply = a * b
print("Multiplication of", a, "and", b, "is:", multiply)  # Output: Multiplication of 15 and 4 is: 60

# Division (float)
divide = a / b
print("Division of", a, "by", b, "(float) is:", divide)  # Output: Division of 15 by 4 (float) is: 3.75

# Division (floor)
floor_divide = a // b
print("Division of", a, "by", b, "(floor) is:", floor_divide)  # Output: Division of 15 by 4 (floor) is: 3

# Modulus
modulus = a % b
print("Modulus of", a, "and", b, "is:", modulus)  # Output: Modulus of 15 and 4 is: 3

# Exponentiation
exponent = a ** b
print(a, "raised to the power", b, "is:", exponent)  # Output: 15 raised to the power 4 is: 50625
print()