# Demonstrating different number data types in Python

# Integer
int_num = 10
print("Integer value:", int_num)  # Output: Integer value: 10
print("Type of integer:", type(int_num))  # Output: Type of integer: <class 'int'>

# Float
float_num = 10.5
print("\nFloat value:", float_num)  # Output: Float value: 10.5
print("Type of float:", type(float_num))  # Output: Type of float: <class 'float'>

# Complex
complex_num = 2 + 3j
print("\nComplex number:", complex_num)  # Output: Complex number: (2+3j)
print("Type of complex number:", type(complex_num))  # Output: Type of complex number: <class 'complex'>

# Boolean
bool_value = True
print("\nBoolean value:", bool_value)  # Output: Boolean value: True
print("Type of boolean:", type(bool_value))  # Output: Type of boolean: <class 'bool'>
